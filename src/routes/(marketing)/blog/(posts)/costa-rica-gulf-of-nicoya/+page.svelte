<p class="lead">
  BUOY.fish is planning a deployment in the Gulf of Nicoya, Costa Rica — bringing connected
  fishing gear tracking to Central American artisanal fisheries for the first time, in
  partnership with the Global Ghost Gear Initiative.
</p>

<h3>The Gulf of Nicoya</h3>

<p>
  The Gulf of Nicoya is one of Costa Rica's most important fishing grounds, supporting
  thousands of artisanal fishermen and their communities. It's also a complex, vast body
  of water where gear loss is a persistent problem — and where the environmental consequences
  of ghost gear compound in a region rich with marine biodiversity.
</p>

<p>
  Our goal is to establish reliable LoRaWAN coverage for approximately 90% of the Gulf,
  providing the wireless backbone for smart buoy tracking across the region's fisheries.
</p>

<h3>Applying What We've Learned</h3>

<p>
  Every deployment teaches us something that shapes the next one. The Costa Rica project
  is designed around key lessons from our
  <a href="/blog/punta-abreojos-deployment">Punta Abreojos</a> and
  <a href="/blog/nova-scotia-trial">Nova Scotia</a> deployments:
</p>

<ul>
  <li><strong>Height and line-of-sight are paramount.</strong> In Nova Scotia, low-lying port facilities underperformed due to "land shadows," while elevated sites provided exponentially better coverage. In Costa Rica, we're prioritizing high-elevation installations — including a lighthouse in Puntarenas and hilltop locations on Isla Chira.</li>
  <li><strong>The scale of the environment demands creative solutions.</strong> The Gulf of Nicoya is vast, and land-based towers alone will leave blind spots. We're augmenting terrestrial coverage with vessel-based gateways for the first time.</li>
  <li><strong>Local relationships are everything.</strong> Securing installation sites requires trust and embedded local partners. GGGI and local NGOs are brokering introductions and permissions before our team arrives.</li>
</ul>

<h3>The Plan</h3>

<p>
  The deployment will install 5 LoRaWAN gateways across strategic sites ringing the Gulf:
</p>

<ul>
  <li><strong>Isla Chira (South)</strong> — at an elevated homestay with excellent line-of-sight across the Gulf</li>
  <li><strong>Isla Chira (North)</strong> — an off-grid solar-powered relay with LTE backhaul, reaching waters inaccessible from shore</li>
  <li><strong>Puntarenas Lighthouse</strong> — our ideal site, identified after our Nova Scotia experience proved lighthouses provide optimal coverage geometry</li>
  <li><strong>Casa Faro Azul (Mainland)</strong> — a hotel rooftop site with power and internet infrastructure</li>
  <li><strong>Nicoya Peninsula hillside</strong> — a restaurant and mirador with exceptional line-of-sight through the inner bay and out to open ocean</li>
</ul>

<p>
  We'll deploy 20 smart buoys and 10 vessel trackers, with 12 months of platform connectivity.
  The total project cost is estimated at $32,227 — demonstrating the affordability of this
  approach compared to satellite-based alternatives that would cost many times more for
  equivalent coverage.
</p>

<h3>Why Costa Rica</h3>

<p>
  Expanding to Central America is a strategic milestone for BUOY.fish. It proves the
  technology can adapt to different geographies, fishery types, and operational contexts.
  The Gulf of Nicoya's artisanal fisheries operate differently from Baja's cooperative
  lobster fleet or Nova Scotia's regulated lobster zones — and understanding those
  differences is how we build a product that works for fishing communities everywhere.
</p>

<p>
  If this deployment succeeds, it opens the door to broader adoption across Central and
  South American fisheries — regions where ghost gear is a massive problem but where the
  cost of satellite tracking has been prohibitive.
</p>

