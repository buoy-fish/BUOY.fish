<p class="lead">
  BUOY.fish is deploying 60 smart buoys in Nova Scotia's Lobster Fishing Area 35
  (LFA35), partnering with Go Deep International, the Global Ghost Gear
  Initiative, Canada's Department of Fisheries and Oceans (DFO), and IRNAS to
  tackle ghost gear in one of the most ecologically critical marine habitats in
  the world.
</p>

<h3>Why Nova Scotia — and Why It Matters</h3>

<p>
  The Bay of Fundy, which includes portions of LFA35, is one of the most
  productive marine systems on earth — and one of the most critical habitats for
  the endangered
  <strong>North Atlantic Right Whale</strong>. With fewer than 400 individuals
  remaining, this species is on the brink.
</p>

<p>
  Nearly <strong
    >90% of right whales have been entangled in fishing gear at least once</strong
  >. Some have been entangled as many as nine times. In 2022, one of the 372
  remaining individuals was observed entangled next to her calf. She is presumed
  dead. Gear entanglements remain the leading cause of death for this critically
  endangered species.
</p>

<p>
  In some surveys, ghost gear accounts for <strong
    >over 70% of anthropogenic debris on the seafloor</strong
  >. Preventing gear loss isn't just about protecting fishermen's livelihoods —
  it's directly connected to the survival of a species.
</p>

<h3>The Deployment</h3>

<p>
  Building on our <a href="/blog/punta-abreojos-deployment"
    >proven deployment in Punta Abreojos, Mexico</a
  >, we're bringing next-generation smart buoys to Atlantic Canadian waters. The
  Nova Scotia project includes:
</p>

<ul>
  <li>
    <strong>3 long-range outdoor LoRaWAN gateways</strong> installed at strategic
    elevated sites
  </li>
  <li>
    <strong>5 vessel trackers</strong> (mappers) installed on fishing boats
  </li>
  <li>
    <strong>60 smart buoys</strong> — resin-housed, inductively charged GPS trackers
    with Bluetooth trap tag synchronization — deployed across 5 local fishing vessels
    (12 per vessel)
  </li>
  <li><strong>12 months of connectivity and platform services</strong></li>
</ul>

<p>
  The smart buoys represent a significant evolution from our Baja deployment.
  They feature a redesigned inductive charging system that allows complete
  potting (waterproofing with two-part expanding foam), eliminating any
  potential for water ingress. Firmware improvements preserve network session
  state during outages, so buoys reconnect faster when coverage returns.
</p>

<h3>Hardware Built to Last</h3>

<p>
  Our buoys are built on proven blow-molded resin plastic hardshell designs,
  manufactured by Tidal Marine (Go Deep International) in New Brunswick — a
  company that has shipped over one million lobster fishing floats to North
  Atlantic fisheries. The tracking electronics are embedded inside the spindle
  (the stick portion of the buoy), counterweighted with a 3/4" stainless
  shackle. The design is rated to last 10-15 years.
</p>

<p>
  The hardware is engineered in partnership with <strong>IRNAS</strong>, a
  leading applied R&D firm in Slovenia with 16 engineers specializing in PCB
  assembly, firmware development, and IoT systems. IRNAS is an official
  development partner of Nordic Semiconductor and a silver member of the Zephyr
  Project.
</p>

<h3>New Capabilities Being Tested</h3>

<p>Nova Scotia is the testing ground for several new technologies:</p>

<ul>
  <li>
    <strong>Bluetooth intelligent trap tags</strong> — small tags attached to individual
    traps that broadcast a unique ID and synchronize with smart buoys overhead, creating
    a digital audit trail for every pot deployment and retrieval. First of its kind.
  </li>
  <li>
    <strong>Direct LoRa-to-vessel communication</strong> — a specialized appliance
    for NMEA 2000-compatible vessel hardware that lets boats see their buoys at 10-30
    mile ranges, even outside LoRaWAN or satellite coverage.
  </li>
  <li>
    <strong>AIS broadcast support</strong> — for extreme offshore deployments where
    vessels operate hundreds of miles from shore.
  </li>
</ul>

<h3>On the Ground</h3>

<p>
  In October 2025, two BUOY.fish team members will travel to Digby, Nova Scotia
  for an 11-day field deployment — installing gateways, setting up vessel
  trackers, training local fishers in a hands-on workshop, and joining them for
  the first deployment at sea.
</p>

<p>
  Key lessons from our Baja deployment are shaping this one. Antenna height and
  line-of-sight are the most critical factors for coverage range — so we're
  prioritizing elevated installation sites. And we've learned that local
  relationships are everything: Go Deep International and GGGI's network are
  essential for securing sites and building trust with the fishing community.
</p>

<h3>The Bigger Picture</h3>

<p>
  The U.S. seafood industry alone supports over 1.7 million jobs and generates
  $255 billion in annual sales. Without action on ghost gear and whale
  entanglements, regulated domestic fisheries face reduced seasons, closures, or
  complete shutdowns — pushing demand to imported seafood from regions with far
  weaker environmental enforcement.
</p>

<p>
  BUOY.fish offers a cost-effective alternative to the status quo. Traditional
  satellite buoys exceed $2,000 per unit with $30-100/month service fees.
  "Pop-up" ropeless fishing gear — while promising in concept — costs roughly
  10x a traditional trap and, as one fisheries commissioner put it, "the only
  one we've tried is still at the bottom of the bay." Our LoRa-based approach
  provides comparable tracking at a fraction of the cost, with network fees
  measured in fractions of a cent per data packet.
</p>

