<h3>The Ghost Gear Crisis</h3>

<p>
  Research published in <em>Science Advances</em> estimates that <strong>more than 25 million
  pots and traps are lost to the ocean every year</strong>. Industry analysts put annual loss
  rates at 10-20% of all deployed gear — meaning hundreds of millions of the estimated one
  billion pots deployed annually vanish beneath the surface.
</p>

<p>
  Unlike other forms of ocean plastic, this "ghost gear" is purpose-built to catch. Lost
  pots and traps continue trapping and killing marine life for years after they're abandoned.
  In the Mid-Atlantic alone, derelict crab pots kill an estimated 3.3 million blue crabs
  each year. Buoy lines ensnare endangered whales. And because ghost gear is designed to
  survive harsh ocean environments, it far outlasts other forms of marine debris.
</p>

<p>
  Nearly <strong>90% of North Atlantic Right Whales</strong> — with fewer than 400 individuals
  remaining — have been entangled in fishing gear at least once. Some as many as nine times.
  Gear entanglements remain the leading cause of death for this critically endangered species.
</p>

<h3>Why Existing Solutions Fall Short</h3>

<p>
  Several organizations have tried satellite-based tracking for fishing gear. The technology
  works, but the economics are prohibitive: a single satellite buoy can exceed <strong>$2,000
  in hardware with $30-100/month in service fees</strong>. A traditional fishing buoy costs
  about $10. No fisherman is going to put a $2,000 tracker on every pot.
</p>

<p>
  "Pop-up" ropeless fishing gear has received significant government investment — California's
  Ocean Protection Council approved up to $1.825 million for the concept. But as one fisheries
  commissioner noted, "the only one we've tried is still at the bottom of the bay." Pop-up
  gear costs roughly 10x a traditional trap and has proven unreliable in real-world conditions.
</p>

<p>
  Meanwhile, cellular connectivity becomes unusable past 2-3 miles from shore, and short-range
  protocols like WiFi and Bluetooth are irrelevant before a boat leaves the harbor.
</p>

<h3>Our Approach: LoRaWAN</h3>

<p>
  BUOY.fish uses Low Power, Long Range Wide Area Networking (LoRaWAN) to bridge the gap.
  Our smart buoys transmit GPS coordinates over <strong>40+ miles of open ocean</strong> at
  a fraction of a cent per data packet. No cellular plan. No satellite subscription. The
  economics finally work for commercial fishing.
</p>

<p>
  Our smart buoys are built on proven blow-molded resin shells manufactured by Tidal Marine
  (Go Deep International), who have shipped over one million lobster fishing floats to North
  Atlantic fisheries. The GPS tracking electronics are embedded inside the spindle, fully
  potted with two-part expanding resin via an inductive charging system — no ports, no seals
  to fail. Designed to last 10-15 years.
</p>

