<p class="lead">
  BUOY.fish is returning to Baja California. Next month, we'll deploy connected smart buoys
  in Punta Eugenia and Isla Natividad — expanding our presence in the region's spiny lobster
  fishery and applying every lesson learned from our first deployment in
  <a href="/blog/punta-abreojos-deployment">Punta Abreojos</a>.
</p>

<h3>Building on Proven Results</h3>

<p>
  Our Punta Abreojos pilot proved the technology works at scale: 80 smart buoys deployed,
  160,000+ location payloads recorded, fishermen adopting the equipment into their daily
  routines. But one deployment in one community was always just the beginning.
</p>

<p>
  Punta Eugenia and Isla Natividad are part of the same network of Baja fishing cooperatives
  managed through Fedecoop — communities that share the same waters, the same species, and
  the same challenges with lost gear. Expanding here lets us test at greater scale while
  building a regional picture of gear movement and loss patterns across multiple fishing zones.
</p>

<h3>What's New This Time</h3>

<p>
  This deployment features significant product improvements developed in partnership with
  IRNAS, our hardware engineering partner:
</p>

<ul>
  <li><strong>Redesigned inductive charging</strong> — the buoys are now fully potted with two-part expanding resin, completely eliminating water ingress. No charging ports, no seals to fail.</li>
  <li><strong>Improved firmware</strong> that preserves network session state during coverage gaps, so buoys reconnect faster when they return to range.</li>
  <li><strong>Hardened spindle design</strong> refined based on field feedback from Punta Abreojos fishermen.</li>
</ul>

<p>
  We're also deploying with the benefit of the LoRaWAN infrastructure already installed in the
  region. The three outdoor towers and indoor gateways from our first deployment provide a
  coverage foundation that extends to the Punta Eugenia and Isla Natividad fishing grounds.
</p>

<h3>The Cooperatives</h3>

<p>
  Both communities are part of Baja's remarkable cooperative fishing system. These
  cooperatives have managed the spiny lobster fishery sustainably for decades — they
  understand gear stewardship, and they've seen what ghost gear does to their fishing grounds.
  The enthusiasm we saw in Punta Abreojos, where fishermen painted our buoys in their
  traditional colors to claim them as their own, gives us confidence that adoption in
  neighboring communities will follow.
</p>

<h3>Funded by GGGI and SMTP</h3>

<p>
  This expansion is funded through continued partnership with the Global Ghost Gear Initiative
  and Schmidt Marine Technology Partners, who earmarked $7,000 specifically for Baja expansion
  as part of a broader $154,000+ commitment to BUOY.fish's 2025 program. The investment
  reflects confidence in the results from Punta Abreojos and the potential for this technology
  to scale across Latin American fisheries.
</p>

<h3>What We'll Measure</h3>

<p>
  As with every deployment, we'll track coverage performance, buoy uptime, ping frequency,
  and real-world gear loss rates against the industry average of 10-20%. In Punta Abreojos,
  cooperatives self-reported 2.5% annual loss — we're aiming to push that lower and, more
  importantly, to give fishermen the tools to recover gear before it becomes ghost gear.
</p>

