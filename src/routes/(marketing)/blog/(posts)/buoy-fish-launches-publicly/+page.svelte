<h3>How We Got Here</h3>

<p>
  BUOY.fish started under the Golden Gate Bridge, where co-founder Jameson
  Buffmire lost his first crab pot. He thought he was just careless — but it
  turns out lost gear is one of the biggest environmental problems in commercial
  fishing. Research in <em>Science Advances</em> estimates
  <strong
    >more than 25 million pots and traps are lost to the ocean annually</strong
  >. In the Mid-Atlantic alone, derelict crab pots kill an estimated 3.3 million
  blue crabs each year.
</p>

<p>
  Jameson co-owns AINU, a 22-foot commercial fishing vessel in San Francisco
  with salmon and groundfish permits. Co-founder Tal McGowan studied Fisheries
  Biology at the University of Tasmania and holds an MBA from Deakin University.
  Together, they founded BUOY.fish in 2023 with a simple premise: if we can
  track packages, pets, and cars, we should be able to track fishing gear.
</p>

<h3>Why Not Satellite?</h3>

<p>
  Several companies have tried satellite-based tracking for fishing gear. The
  technology works, but the economics don't — a single satellite buoy can exceed
  $2,000 in hardware with $30-100/month in service fees. A traditional fishing
  buoy costs about $10. No fisherman is going to attach a $2,000 tracker to a
  $10 buoy.
</p>

<p>
  BUOY.fish uses LoRaWAN — a low-power, long-range wireless protocol — to
  transmit GPS coordinates over 30+ miles of open ocean at a fraction of a cent
  per data packet. No cellular plan, no satellite subscription. The result is a
  smart buoy that's economically viable for commercial fishing, not just
  research projects.
</p>

<h3>First Sales</h3>

<p>
  In Q1 2024, we made our first commercial sale to Bay Area Dungeness crab
  fishermen. California's Department of Fish and Wildlife has approved BUOY.fish
  technology for deployment in the California Dungeness Crab fishery — and our
  tracking system has already been used to identify and apprehend a poacher who
  removed trackable units from the water.
</p>

